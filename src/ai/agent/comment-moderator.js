/**
 * Comment Moderator — oculta automáticamente comentarios de ads que matchean reglas.
 *
 * Caso principal (2026-06-20): los creativos son generados con IA → la gente comenta
 * "esto es IA / fake / AI generated" y espanta compradores. Esto los OCULTA (is_hidden,
 * reversible, el autor no se entera) automáticamente. También una blocklist custom.
 *
 * SHADOW por default: loguea qué ocultaría (action='would_hide') sin tocar nada. El creador
 * revisa en el panel que no se coma comentarios legítimos y recién ahí pasa a live.
 *
 * Config en SystemConfig 'comment_moderation':
 *   { enabled, shadow, ai_callouts, ai_words[], ai_phrases[], custom_words[], custom_phrases[] }
 * Acción solo del lado Facebook (IG usa otro grafo — fase 2).
 */
const logger = require('../../utils/logger');
const SystemConfig = require('../../db/models/SystemConfig');
const MetricSnapshot = require('../../db/models/MetricSnapshot');
const CommentModerationLog = require('../../db/models/CommentModerationLog');

const CONFIG_KEY = 'comment_moderation';
const MAX_ADS_PER_CYCLE = parseInt(process.env.COMMENT_MOD_MAX_ADS || '150', 10);
const COMMENTS_PER_AD = parseInt(process.env.COMMENT_MOD_COMMENTS_PER_AD || '50', 10);

// Seed por default — editable desde el panel/SystemConfig.
const DEFAULT_CONFIG = {
  enabled: true,
  shadow: true,            // arranca en shadow SIEMPRE
  ai_callouts: true,
  // palabras sueltas (match con WORD-BOUNDARY — "ai" no pega en "again"/"email")
  ai_words: ['ai', 'ia'],
  // frases / substrings (match por inclusión)
  ai_phrases: [
    'ai generated', 'ai-generated', 'generated by ai', 'generated with ai', 'made with ai',
    'generado por ia', 'generada por ia', 'hecho con ia', 'hecha con ia', 'imagen de ia',
    'inteligencia artificial', 'ai slop', 'ai art', 'ai image', 'ai pic',
    'midjourney', 'chatgpt', 'dall-e', 'dalle', 'stable diffusion', '🤖',
    'fake', 'not real', 'no es real', 'es falso', 'es fake', 'parece ia'
  ],
  custom_words: [],        // blocklist del creador (word-boundary)
  custom_phrases: []       // blocklist del creador (substring)
};

async function getConfig() {
  const saved = await SystemConfig.get(CONFIG_KEY, null);
  if (!saved) return { ...DEFAULT_CONFIG };
  // merge defensivo: si faltan campos nuevos, completar con default
  return { ...DEFAULT_CONFIG, ...saved };
}

// lowercase + sin acentos, para matchear "IA"/"ía"/"Iá" parejo
function normalize(s) {
  return (s || '').toLowerCase().normalize('NFD').replace(/[̀-ͯ]/g, '');
}
function escapeRe(s) { return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'); }

/**
 * ¿este mensaje matchea alguna regla? Devuelve {rule, term} o null.
 * words → word-boundary (evita falsos positivos en substrings). phrases → inclusión.
 */
function matchComment(message, cfg) {
  const text = normalize(message);
  if (!text.trim()) return null;

  const words = [
    ...(cfg.ai_callouts ? (cfg.ai_words || []).map(w => ({ w, rule: 'ai_callout' })) : []),
    ...(cfg.custom_words || []).map(w => ({ w, rule: 'blocklist' }))
  ];
  const phrases = [
    ...(cfg.ai_callouts ? (cfg.ai_phrases || []).map(p => ({ p, rule: 'ai_callout' })) : []),
    ...(cfg.custom_phrases || []).map(p => ({ p, rule: 'blocklist' }))
  ];

  for (const { p, rule } of phrases) {
    const pn = normalize(p);
    if (pn && text.includes(pn)) return { rule, term: p };
  }
  for (const { w, rule } of words) {
    const wn = normalize(w);
    if (!wn) continue;
    // \b no funciona con emojis ni siempre con unicode; usamos límites de "palabra latina"
    const re = new RegExp(`(^|[^a-z0-9])${escapeRe(wn)}([^a-z0-9]|$)`, 'i');
    if (re.test(text)) return { rule, term: w };
  }
  return null;
}

/**
 * Ads a moderar: los que tienen un snapshot RECIENTE (≤2d) — sin importar si están
 * ACTIVE o PAUSED (el post de un ad pausado SIGUE mostrando sus comentarios públicos →
 * los callouts de IA igual hay que ocultarlos). El filtro de snapshot reciente descarta
 * solo los ads muertos de la cuenta vieja (ya no se recolectan → sin snapshot fresco → 400).
 * Ya NO exige spend>0: un ad sin gasto reciente puede seguir teniendo comentarios visibles.
 */
async function getActiveAdIds(limit) {
  const since = new Date(Date.now() - 2 * 24 * 3600 * 1000);
  const rows = await MetricSnapshot.aggregate([
    { $match: { entity_type: 'ad', snapshot_at: { $gte: since } } },
    { $sort: { entity_id: 1, snapshot_at: -1 } },
    { $group: {
      _id: '$entity_id',
      status: { $first: '$status' },
      spend7d: { $first: '$metrics.last_7d.spend' },
      name: { $first: '$entity_name' }
    }},
    { $sort: { spend7d: -1 } },   // prioriza los de más gasto (más ojos), pero incluye todos
    { $limit: limit }
  ]);
  return rows.map(r => ({ ad_id: r._id, name: r.name }));
}

/**
 * Corre un ciclo de moderación. SHADOW por default (no toca nada, solo loguea).
 * @returns {Object} resumen
 */
async function runCommentModeration() {
  const cfg = await getConfig();
  if (!cfg.enabled) { logger.info('[COMMENT-MOD] deshabilitado'); return { skipped: 'disabled' }; }

  const { getMetaClient } = require('../../meta/client');
  const meta = getMetaClient();

  // Verificar que el page token esté disponible antes de barrer (falla claro si no).
  try { await meta.getPageAccessToken(); }
  catch (e) { logger.warn(`[COMMENT-MOD] sin page token (${e.message}) — abort`); return { skipped: 'no_page_token', error: e.message }; }

  const ads = await getActiveAdIds(MAX_ADS_PER_CYCLE);
  let scanned = 0, matched = 0, hidden = 0, wouldHide = 0, errors = 0, newComments = 0;

  for (const ad of ads) {
    let res;
    try {
      res = await meta.getAdComments(ad.ad_id, { limit: COMMENTS_PER_AD });
    } catch (e) {
      logger.warn(`[COMMENT-MOD] getAdComments ${ad.ad_id} falló: ${e.message}`);
      continue;
    }
    scanned++;
    for (const c of (res.comments || [])) {
      const hit = matchComment(c.message, cfg);
      if (!hit) continue;
      matched++;

      // dedup: si ya lo registramos, no re-procesar (salvo que esté en would_hide y ahora sea live)
      const existing = await CommentModerationLog.findOne({ comment_id: c.id }).lean();
      if (existing && (existing.action === 'hidden' || existing.action === 'unhidden')) continue;

      // Si Meta ya lo reporta oculto (is_hidden=true), no re-hidear — re-ocultar devuelve
      // un 400/1446036 inútil. Lo registramos como hidden y seguimos.
      if (c.is_hidden) {
        if (!existing || existing.action !== 'hidden') {
          await CommentModerationLog.findOneAndUpdate({ comment_id: c.id },
            { comment_id: c.id, ad_id: ad.ad_id, story_id: res.story_id, author_name: c.author_name,
              author_id: c.author_id, message: c.message, matched_rule: hit.rule, matched_term: hit.term,
              shadow: cfg.shadow, comment_created_time: c.created_time, action: 'hidden' }, { upsert: true });
        }
        matched--; // ya estaba oculto, no cuenta como "nuevo a ocultar"
        continue;
      }

      const base = {
        comment_id: c.id, ad_id: ad.ad_id, story_id: res.story_id,
        author_name: c.author_name, author_id: c.author_id, message: c.message,
        matched_rule: hit.rule, matched_term: hit.term, shadow: cfg.shadow,
        comment_created_time: c.created_time
      };

      if (cfg.shadow) {
        if (!existing) {
          await CommentModerationLog.create({ ...base, action: 'would_hide' });
          newComments++; wouldHide++;
          logger.info(`[COMMENT-MOD] (shadow) ocultaría: "${(c.message || '').slice(0, 60)}" — ${hit.rule}:${hit.term} · ad ${ad.ad_id}`);
        }
      } else {
        // LIVE: ocultar de verdad
        try {
          await meta.hideComment(c.id, true);
          await CommentModerationLog.findOneAndUpdate(
            { comment_id: c.id },
            { ...base, action: 'hidden' },
            { upsert: true }
          );
          hidden++; if (!existing) newComments++;
          logger.info(`[COMMENT-MOD] OCULTADO: "${(c.message || '').slice(0, 60)}" — ${hit.rule}:${hit.term} · ad ${ad.ad_id}`);
        } catch (e) {
          // Meta quirk: is_hidden=true a veces devuelve 1446036. INCONSISTENTE: a veces igual
          // oculta, a veces es rate-limit real y NO oculta. NO lo marcamos 'hidden' (si no ocultó,
          // el cron nunca reintentaría). Lo dejamos 'skipped_error' (retriable): el próximo ciclo
          // re-lee is_hidden → si ya quedó oculto lo saltea, si no, reintenta. Así converge solo.
          errors++;
          await CommentModerationLog.findOneAndUpdate(
            { comment_id: c.id }, { ...base, action: 'skipped_error', error: e.message }, { upsert: true });
          const sub = e.response?.data?.error?.error_subcode;
          logger.warn(`[COMMENT-MOD] hide ${c.id} → ${sub === 1446036 ? '1446036 (rate-limit, reintenta próximo ciclo)' : e.message}`);
        }
      }
    }
  }

  const summary = { mode: cfg.shadow ? 'shadow' : 'live', ads_scanned: scanned, matched, hidden, would_hide: wouldHide, new: newComments, errors };
  logger.info(`[COMMENT-MOD] ciclo: ${JSON.stringify(summary)}`);
  return summary;
}

module.exports = { runCommentModeration, matchComment, getConfig, DEFAULT_CONFIG, CONFIG_KEY };
